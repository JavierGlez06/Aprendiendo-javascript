let a = 5;
a = a + 5;

a += 5; // Equivalente a a = a + 5
a -= 2; // Equivalente a a = a - 2
a *= 3; // Equivalente a a = a * 3
a /= 2; // Equivalente a a = a / 2
a %= 3; // Equivalente a a = a % 3
a **= 2; // Equivalente a a = a ** 2
a <<= 1; // Desplazamiento a la izquierda
a >>= 1; // Desplazamiento a la derecha

console.log(a);