let a = 10;
console.log(a > 5); // Mayor que
console.log(a >= 5); // Mayor o igual que
console.log(a < 5); // Menor que
console.log(a <= 5); // Menor o igual que

console.log(a == 10); // Igualdad (valor)
console.log(a != 10); // Desigualdad (valor)
console.log(a == '10'); // Igualdad (valor y tipo)
console.log(a === '10'); // Igualdad estricta (valor y tipo)
console.log(a !== '10'); // Desigualdad estricta (valor y tipo)